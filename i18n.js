// Simpel {placeholder}-erstatning: tf("Vælg {n} tilfældige", {n:4}) -> "Vælg 4 tilfældige"
function tf(str, vars){
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars && vars[k]!==undefined) ? vars[k] : `{${k}}`);
}
